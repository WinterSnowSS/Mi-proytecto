    private void btNewtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNewtonActionPerformed
//X^3-2*X^2-5*X+2
try {
        String func = txtFuncion.getText();
        double x0 = Double.parseDouble(txtX0.getText()); 
        double tol = Double.parseDouble(txtError.getText());
        int maxIter = Integer.parseInt(txtIteraciones.getText());

        // LLAMADA AL MÉTODO DE NEWTON
        String resultado = MetodosNumericos.newtonRaphson(func, x0, tol, maxIter);
        txtResultado.setText(resultado);

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "ERROR: Verifica que los campos de datos sean números válidos.", "Error de Entrada", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "ERROR INESPERADO: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btNewtonActionPerformed

