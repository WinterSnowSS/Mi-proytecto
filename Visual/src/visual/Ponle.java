    private void btEncontrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEncontrarActionPerformed
try {
        // 1. Obtener datos de la interfaz
        String func = txtFuncion.getText();
        // Usamos x0 como Límite Negativo
        double limiteNegativo = Double.parseDouble(txtX0.getText()); 
        // Usamos x1 como Límite Positivo
        double limitePositivo = Double.parseDouble(txtX1.getText()); 
        // Usamos Tolerancia como Delta X
        double deltaX = Double.parseDouble(txtError.getText()); 
        // Iteraciones no se usa en este método
        
        // 2. Ejecutar cálculo
        String resultado = MetodosNumericos.busquedaRaices(func, limiteNegativo, limitePositivo, deltaX);
        txtResultado.setText(resultado);

    } catch (NumberFormatException e) {
        txtResultado.setText("ERROR: Verifica que los campos Límite Negativo (x0), Límite Positivo (x1) y Delta X (Tolerancia) contengan números válidos.");
    } catch (Exception e) {
        txtResultado.setText("ERROR INESPERADO: " + e.getMessage());
    }    
    
        // TODO add your handling code here:
    }//GEN-LAST:event_btEncontrarActionPerformed

